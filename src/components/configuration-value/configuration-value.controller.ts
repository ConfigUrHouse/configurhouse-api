import { ConfigurationValue } from './configuration-value.class';
import { Response, Request, NextFunction } from 'express';
import { ErrorHandler } from '../../middleware/error-handler';

export const findAll = (req: Request, res: Response, next: NextFunction) => {
  ConfigurationValue.findAll()
    .then((data) => {
      res.send(data);
    })
    .catch((err: any) => {
      next(new ErrorHandler(500, 'Message to define'));
    });
};

export const findOne = (req: Request, res: Response, next: NextFunction) => {
  const idValue = req.params.idValue;
  const idConfiguration = req.params.idConfiguration;

  ConfigurationValue.findOne({
    where: {
      id_Value: idValue,
      id_Configuration: idConfiguration
    },
  })
    .then((data) => {
      res.send(data);
    })
    .catch((err: any) => {
      next(new ErrorHandler(500, 'Message to define'));
    });
};

export const update = (req: Request, res: Response, next: NextFunction) => {
  const idValue = req.params.idValue;
  const idConfiguration = req.params.idConfiguration;

  ConfigurationValue.update(req.body, {
    where: {
      id_Value: idValue,
      id_Configuration: idConfiguration
    },
  })
    .then((num: any) => {
      if (num == 1) {
        res.status(201).send({
          message: 'Message to define',
        });
      } else {
        next(new ErrorHandler(500, 'Message to define'));
      }
    })
    .catch((err: any) => {
      next(new ErrorHandler(500, 'Message to define'));
    });
};

